# Combinatorics solution to grid paths problem
# Notes:
# - The ML is too low, we can't just precompute all 10^6 factorials mod 1e9+7
#   - Instead, hardcode every 1000th and manually compute between hardcoded values
#   - This is quite a large time tradeoff for memory optimization, it's fine here since X <= 2
# - This code also works for more than 2 obstacles, it's just slow

### MODULAR ARITHMETIC TEMPLATE START ###
MOD = 10 ** 9 + 7


# MN = 2 * 10 ** 6 + 1
# factorial = [1] * MN  # precompute factorials
# for i in range(2, MN):
#     factorial[i] = (factorial[i - 1] * i) % MOD
# print(factorial[::1000])

def get_factorial(num):
    thousands, rem = divmod(num, 1000)
    res = jumps[thousands]
    for i in range(thousands * 1000 + 1, thousands * 1000 + rem + 1):
        res = (res * i) % MOD
    return res


mod_div = lambda p, r: (p * pow(r, MOD - 2, MOD)) % MOD
comb = lambda n, k: mod_div(get_factorial(n), (get_factorial(k) * get_factorial(n - k))) if n >= k else 0  # N choose K
paths = lambda r1, c1, r2, c2: comb((r2 - r1) + (c2 - c1), (r2 - r1))
